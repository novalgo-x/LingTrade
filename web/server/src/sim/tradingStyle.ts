export type TradingStyle = "conservative" | "balanced" | "aggressive";

export const TRADING_STYLES: readonly TradingStyle[] = ["conservative", "balanced", "aggressive"];

export function resolveTradingStyle(value: unknown): TradingStyle {
  if (value === "conservative" || value === "balanced" || value === "aggressive") return value;
  return "balanced";
}

